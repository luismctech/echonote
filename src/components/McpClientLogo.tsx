/**
 * `McpClientLogo` — official brand logos for MCP client applications.
 *
 * Sources:
 *   Claude   — simple-icons / Anthropic brand assets
 *   VS Code  — Wikimedia Commons (official Microsoft SVG)
 *   Cursor   — simple-icons (hexagonal mark, cursor.com/brand)
 *   Windsurf — simple-icons (Codeium/Windsurf "W" wave mark)
 */

export function McpClientLogo({
  clientId,
  size = 20,
}: Readonly<{
  clientId: string;
  size?: number;
}>) {
  switch (clientId) {
    case "claude-desktop":
    case "claude-code":
      return <ClaudeLogo size={size} />;
    case "vscode":
      return <VSCodeLogo size={size} />;
    case "cursor":
      return <CursorLogo size={size} />;
    case "windsurf":
      return <WindsurfLogo size={size} />;
    default:
      return <DefaultLogo size={size} />;
  }
}

